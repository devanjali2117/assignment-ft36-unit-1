### Problem 2: Tuple Reversal (Not to use Inbuilt Function)

# **Description:** Write a Python program to reverse the elements of a tuple.

# **Sample Input:** `(1, 2, 3, 4, 5)`

# **Sample Output:** `(5, 4, 3, 2, 1)`

sample_tuple = (1, 2, 3, 4, 5)

reversed_tuple = sample_tuple[::-1]

print(reversed_tuple)
